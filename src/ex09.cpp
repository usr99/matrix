#include "Tests.hpp"

void ex09()
{
	{
		Matrix<3, 3, int> u({
			{ 1, 2, 3 },
			{ 4, 5, 6 },
			{ 7, 8, 9 }
		});
		std::cout << u.transpose() << '\n';
	}

	{
		Matrix<2, 3, char> u({
			{ 'a', 'b', 'c' },
			{ 'd', 'e', 'f' }
		});
		std::cout << u.transpose() << '\n';
	}

	{
		Matrix<5, 3, int> u({
			{ 1, 4, 3 },
			{ 8, 2, 6 },
			{ 7, 8, 3 },
			{ 4, 9, 6 },
			{ 7, 8, 1 }
		});
		std::cout << u.transpose() << '\n';
	}	
}
