#include "Tests.hpp"

void ex13()
{
	{
		Matrix<3, 3, double> u;
		std::cout << u.rank() << '\n';
	}

	{
		Matrix<3, 4, double> u({
			{  1, 2, 0, 0 },
			{  2, 4, 0, 0 },
			{ -1, 2, 1, 1 },
		});
		std::cout << u.rank() << '\n';
	}

	{
		Matrix<4, 3, double> u({
			{ 8,  5,  -2 },
			{ 4,  7,  20 },
			{ 7,  6,  1 },
			{ 21, 18, 7 },
		});
		std::cout << u.rank() << '\n';
	}

	{
		Matrix<3, 5, double> u({
			{8., 5., -2., 4., 28.},
			{4., 2.5, 20., 4., -4.},
			{8., 5., 1., 4., 17.}
		});

		std::cout << u.rank() << '\n';
	}	
}
